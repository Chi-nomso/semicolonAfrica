package VariousPractice;

public class LoopExample {
    public static void main(String[] args) {
        String today = "Wed";

        switch (today) {
            case "Mon":
                System.out.println("Mon");
                //break;
            case "Tue":
                System.out.println("Tue");
                //break;
            case "Wed":
                System.out.println("Wed");
                //break;
            default:
                System.out.println("None of the above");
        }
    }
}
